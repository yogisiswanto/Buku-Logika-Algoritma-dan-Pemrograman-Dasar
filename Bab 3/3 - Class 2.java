public class Pukul{

    private int jam;
    private int menit;
    private int detik;

    Pukul(){

    }

    public void setJam(int jam){

        this.jam = jam;
    }

    public float getJam(){

        return this.jam;
    }

    public void setMenit(int menit){

        this.menit = menit;
    }

    public float getMenit(){

        return this.menit;
    }

    public void setDetik(int detik){

        this.detik = detik;
    }

    public float getDetik(){

        return this.detik;
    }
}