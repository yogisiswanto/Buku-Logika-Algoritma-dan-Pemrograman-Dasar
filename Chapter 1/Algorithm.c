#include <stdio.h>

// Main function
int main(){

    // Variable declaration
    int kentang;
    int pisau;

    // Variable inisialization
    kentang = 1;
    pisau = 1;

    //Start the process
    printf("Mulai mengupas kentang\n");

    // Process
    kentang = kentang + pisau;

    // Variable reinisialization
    pisau = 0;

    // End of process
    printf("Selesai mengupas kentang\n");

    return 0;
}