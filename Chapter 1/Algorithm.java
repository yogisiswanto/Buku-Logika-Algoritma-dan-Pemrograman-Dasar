public class Main {

    // Main function
    public static void main(String []args){

        // Variable instantiation
        int kentang = 0;
        int pisau = 0;

        // Variable inisialization
        kentang = 1;
        pisau = 1;

        //Start the process
        System.out.println("Mulai mengupas kentang");

        // Process
        kentang = kentang + pisau;

        // Variable reinisialization
        pisau = 0;
        
        // End of process
        System.out.println("Selesai mengupas kentang");
    }
}